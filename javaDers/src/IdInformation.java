public class IdInformation {

    int idNumber;
    String name;
    String surname;
    String dateOfBirth;

    public IdInformation(int idNumber, String name, String surname, String dateOfBirth) {
        this.idNumber = idNumber;
        this.name = name;
        this.surname = surname;
        this.dateOfBirth = dateOfBirth;
    }
}
